DECLARE
v_PROCESS_ID NUMBER;
v_PROCESS_STATUS NUMBER;
v_MESSAGE VARCHAR2(4000);
v_TIME_ZONES STRING_COLLECTION := STRING_COLLECTION(GA.CUT_TIME_ZONE, GA.LOCAL_TIME_ZONE);
BEGIN
   SECURITY_CONTROLS.SET_CURRENT_USER(SECURITY_CONTROLS.c_SUSER_SYSTEM);
   POPULATE_SYSTEM_DATE_TIME.RUN(
      p_BEGIN_DATE      => TO_DATE('1/1/2019','MM/DD/YYYY'),
      p_END_DATE        => TO_DATE('12/31/2024','MM/DD/YYYY'),
      p_TIME_ZONES      => v_TIME_ZONES,
      p_PROCESS_ID      => v_PROCESS_ID,
      p_PROCESS_STATUS  => v_PROCESS_STATUS,
      p_MESSAGE         => v_MESSAGE);
   COMMIT;
END;
/

MERGE INTO CDI_PLC_WEATHER_NORMAL_FACTOR T
USING (
   SELECT 'G' "RATE_CLASS",  'SEC' "VOLTAGE_LEVEL",    1.028206 "FACTOR" FROM DUAL UNION
   SELECT 'G',  'X13 kV', 1.028206 FROM DUAL UNION
   SELECT 'GF', 'SEC',    0.973959 FROM DUAL UNION
   SELECT 'GL', 'SEC',    1.010238 FROM DUAL UNION
   SELECT 'GL', 'X13 kV', 0.970499 FROM DUAL UNION
   SELECT 'GL', 'X34 kV', 0.970499 FROM DUAL UNION
   SELECT 'GS', 'SEC',    1.001140 FROM DUAL UNION
   SELECT 'NR', '230 kV', 0.951859 FROM DUAL UNION
   SELECT 'P',  '115 kV', 0.969271 FROM DUAL UNION
   SELECT 'P',  'X13 kV', 0.969271 FROM DUAL UNION
   SELECT 'P',  'X34 kV', 0.969271 FROM DUAL) S
ON (T.RATE_CLASS = S.RATE_CLASS AND T.VOLTAGE_LEVEL = S.VOLTAGE_LEVEL)
WHEN NOT MATCHED THEN
   INSERT(RATE_CLASS, VOLTAGE_LEVEL, FACTOR)
   VALUES(S.RATE_CLASS, S.VOLTAGE_LEVEL, S.FACTOR);
COMMIT;

CREATE SEQUENCE CDI_WEATHER_PARAMETER_STG_ID INCREMENT BY 1;

BEGIN CDI_DROP_OBJECT('CDI_WEATHER_PARAMETER_STAGING','TABLE'); END;
/

CREATE TABLE CDI_WEATHER_PARAMETER_STAGING
(
  STAGING_CONTENT   VARCHAR2(1000),
  STAGING_ID        NUMBER(38),
  STAGING_TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) TABLESPACE &&NERO_DATA_TABLESPACE;

CREATE OR REPLACE TRIGGER CDI_WEATHER_PARAMETER_STG_TRG
   BEFORE INSERT ON CDI_WEATHER_PARAMETER_STAGING FOR EACH ROW
BEGIN
   :new.STAGING_ID := CDI_WEATHER_PARAMETER_STG_ID.NEXTVAL;
END;

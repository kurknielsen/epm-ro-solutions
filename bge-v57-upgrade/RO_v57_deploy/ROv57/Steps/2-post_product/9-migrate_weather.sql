DECLARE
v_STATION_ID NUMBER(9) := 101;
v_PARAMETER_ID NUMBER(9) := 101;
BEGIN
   FOR v_SELECT IN (SELECT STATION_ID FROM WEATHER_STATION WHERE STATION_ID > 0 ORDER BY STATION_NAME) LOOP
      UPDATE WEATHER_STATION SET STATION_ID = v_STATION_ID WHERE STATION_ID = v_SELECT.STATION_ID;
      UPDATE WEATHER_STATION_PARAMETER SET STATION_ID = v_STATION_ID WHERE STATION_ID = v_SELECT.STATION_ID;
      UPDATE WEATHER_STATION_COMPOSITE SET STATION_ID = v_STATION_ID WHERE STATION_ID = v_SELECT.STATION_ID;
      UPDATE WEATHER_STATION_COMPOSITE SET COMPOSITE_STATION_ID = v_STATION_ID WHERE COMPOSITE_STATION_ID = v_SELECT.STATION_ID;
      UPDATE STATION_PARAMETER_VALUE SET STATION_ID = v_STATION_ID WHERE STATION_ID = v_SELECT.STATION_ID;
      UPDATE SERVICE_LOCATION SET WEATHER_STATION_ID = v_STATION_ID WHERE WEATHER_STATION_ID = v_SELECT.STATION_ID;
      v_STATION_ID := v_STATION_ID + 1;
   END LOOP;
   FOR v_SELECT IN (SELECT PARAMETER_ID FROM WEATHER_PARAMETER WHERE PARAMETER_ID > 0 ORDER BY PARAMETER_NAME) LOOP
      UPDATE WEATHER_PARAMETER SET PARAMETER_ID = v_PARAMETER_ID WHERE PARAMETER_ID = v_SELECT.PARAMETER_ID;
      UPDATE WEATHER_STATION_PARAMETER SET PARAMETER_ID = v_PARAMETER_ID WHERE PARAMETER_ID = v_SELECT.PARAMETER_ID;
      UPDATE STATION_PARAMETER_VALUE SET PARAMETER_ID = v_PARAMETER_ID WHERE PARAMETER_ID = v_SELECT.PARAMETER_ID;
      v_PARAMETER_ID := v_PARAMETER_ID + 1;
   END LOOP; 
   COMMIT;
END;

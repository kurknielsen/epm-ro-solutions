CREATE OR REPLACE TYPE COLUMN_TYPE AS OBJECT
	(
--Revision: $Revision: 1.12 $
	C1 VARCHAR(32),
	C2 VARCHAR(32),
	C3 VARCHAR(32),
	C4 VARCHAR(32),
	C5 VARCHAR(32),
	C6 VARCHAR(32),
	C7 VARCHAR(32),
	C8 VARCHAR(32),
	C9 VARCHAR(32),
	C10 VARCHAR(32),
	C11 VARCHAR(32),
	C12 VARCHAR(32),
	DTE DATE,
	MEMBER PROCEDURE PUT(p_COL IN NUMBER, p_VAL IN VARCHAR),
	MEMBER PROCEDURE PUT(p_DATE IN DATE),
	MEMBER FUNCTION PRINT RETURN VARCHAR 
	);
/

CREATE OR REPLACE TYPE BODY COLUMN_TYPE AS
MEMBER PROCEDURE PUT(p_COL IN NUMBER, p_VAL IN VARCHAR) AS
BEGIN
	IF p_COL = 1 THEN
		C1 := p_VAL;
	ELSIF p_COL = 2 THEN
		C2 := p_VAL;
	ELSIF p_COL = 3 THEN
		C3 := p_VAL;
	ELSIF p_COL = 4 THEN
		C4 := p_VAL;
	ELSIF p_COL = 5 THEN
		C5 := p_VAL;
	ELSIF p_COL = 6 THEN
		C6 := p_VAL;
	ELSIF p_COL = 7 THEN
		C7 := p_VAL;
	ELSIF p_COL = 8 THEN
		C8 := p_VAL;
	ELSIF p_COL = 9 THEN
		C9 := p_VAL;
	ELSIF p_COL = 10 THEN
		C10 := p_VAL;
	ELSIF p_COL = 11 THEN
		C11 := p_VAL;
	ELSIF p_COL = 12 THEN
		C12 := p_VAL;
	END IF;
END PUT;
MEMBER PROCEDURE PUT(p_DATE IN DATE) AS
BEGIN
	DTE := p_DATE;
END PUT;
MEMBER FUNCTION PRINT RETURN VARCHAR IS
BEGIN
	RETURN C1 || ',' || C2 || ',' || C3 || ',' || C4 || ',' || C5 || ',' || C6 || ',' || C7 || ',' || C8 || ',' || C9 || ',' || C10 || ',' || C11|| ',' || C12;
END PRINT;
END;
/

CREATE OR REPLACE TYPE COLUMN_TABLE IS TABLE OF COLUMN_TYPE;
/

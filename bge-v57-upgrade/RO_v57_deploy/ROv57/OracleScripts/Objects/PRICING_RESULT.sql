CREATE OR REPLACE TYPE PRICING_RESULT AS OBJECT
(
--Revision: $Revision: 1.2 $
	PRODUCT_ID			NUMBER(9),
	COMPONENT_ID		NUMBER(9),
	TAXED_PRODUCT_ID	NUMBER(9),
	TAXED_COMPONENT_ID	NUMBER(9),
	CHILD_COMPONENT_ID	NUMBER(9),
	PERIOD_ID			NUMBER(9),
	BAND_TIER_NUMBER	NUMBER(3),
	BEGIN_DATE			DATE,
	END_DATE			DATE,
	DATES_ARE_CUT		NUMBER(1),
	NUMBER_OF_INTERVALS	NUMBER,
	BASE_QUANTITY		NUMBER,
	FACTOR				NUMBER,
	QUANTITY			NUMBER,
	RATE				NUMBER,
	AMOUNT				NUMBER,
	FML_CHARGE_ID		NUMBER(12),
	DETERMINANT_STATUS	NUMBER(1),
	CONSTRUCTOR FUNCTION PRICING_RESULT RETURN SELF AS RESULT,
	CONSTRUCTOR FUNCTION PRICING_RESULT(p_OTHER IN PRICING_RESULT) RETURN SELF AS RESULT
);
/
CREATE OR REPLACE TYPE BODY PRICING_RESULT IS
-------------------------------------------------------------------------------
CONSTRUCTOR FUNCTION PRICING_RESULT RETURN SELF AS RESULT IS
BEGIN
	-- leave all members NULL
	RETURN;
END PRICING_RESULT;
-------------------------------------------------------------------------------
CONSTRUCTOR FUNCTION PRICING_RESULT(p_OTHER IN PRICING_RESULT) RETURN SELF AS RESULT IS
BEGIN
	-- Copy all members from incoming object
	SELF.PRODUCT_ID := p_OTHER.PRODUCT_ID;
	SELF.COMPONENT_ID := p_OTHER.COMPONENT_ID;
	SELF.TAXED_PRODUCT_ID := p_OTHER.TAXED_PRODUCT_ID;
	SELF.TAXED_COMPONENT_ID := p_OTHER.TAXED_COMPONENT_ID;
	SELF.CHILD_COMPONENT_ID	:= p_OTHER.CHILD_COMPONENT_ID;
	SELF.PERIOD_ID := p_OTHER.PERIOD_ID;
	SELF.BAND_TIER_NUMBER := p_OTHER.BAND_TIER_NUMBER;
	SELF.BEGIN_DATE	:= p_OTHER.BEGIN_DATE;
	SELF.END_DATE := p_OTHER.END_DATE;
	SELF.NUMBER_OF_INTERVALS := p_OTHER.NUMBER_OF_INTERVALS;
	SELF.BASE_QUANTITY := p_OTHER.BASE_QUANTITY;
	SELF.FACTOR := p_OTHER.FACTOR;
	SELF.QUANTITY := p_OTHER.QUANTITY;
	SELF.RATE := p_OTHER.RATE;
	SELF.AMOUNT := p_OTHER.AMOUNT;
	SELF.FML_CHARGE_ID := p_OTHER.FML_CHARGE_ID;
	SELF.DETERMINANT_STATUS := p_OTHER.DETERMINANT_STATUS;
	SELF.DATES_ARE_CUT := p_OTHER.DATES_ARE_CUT;
	
	RETURN;
END PRICING_RESULT;
-------------------------------------------------------------------------------
END;
/
CREATE OR REPLACE TYPE PRICING_RESULT_TABLE AS TABLE OF PRICING_RESULT;
/

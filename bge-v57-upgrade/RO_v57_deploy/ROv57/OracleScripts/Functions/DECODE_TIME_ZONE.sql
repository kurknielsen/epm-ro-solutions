CREATE OR REPLACE FUNCTION DECODE_TIME_ZONE
	(
	p_TIME_ZONE IN VARCHAR
	) RETURN VARCHAR IS
--Revision: $Revision: 1.18 $

v_TIME_ZONE VARCHAR(3);

BEGIN
	IF UPPER(p_TIME_ZONE) IN ('ADT','AST','EDT','EST','CDT','CST','MDT','MST','PDT','PST','GMT') THEN
		v_TIME_ZONE :=  UPPER(p_TIME_ZONE);
	ELSE
		SELECT DECODE(UPPER(LTRIM(RTRIM(p_TIME_ZONE))),
			'ATLANTIC','ADT',
			'EASTERN','EDT',
			'CENTRAL','CDT',
			'INDIANA','CST',
			'MOUNTAIN','MDT',
			'ARIZONA','MST',
			'PACIFIC','PDT',
			LOCAL_TIME_ZONE)
		INTO v_TIME_ZONE
		FROM DUAL;
	END IF;

	RETURN v_TIME_ZONE;

END DECODE_TIME_ZONE;
/


CREATE OR REPLACE FUNCTION MARKET_PRICE_AS_OF_DATE
	(
	p_MARKET_PRICE_ID IN NUMBER,
	p_PRICE_CODE IN CHAR,
	p_PRICE_DATE IN DATE,
	p_AS_OF_DATE IN DATE
	) RETURN DATE IS
--Revision: $Revision: 1.17 $

-- Answer the maximun as of date that is less than or equal to the argument as of date.

v_AS_OF_DATE DATE := LOW_DATE;

BEGIN

	IF GA.VERSION_MARKET_PRICE THEN
		SELECT MAX(AS_OF_DATE)
		INTO v_AS_OF_DATE
		FROM MARKET_PRICE_VALUE
		WHERE MARKET_PRICE_ID = p_MARKET_PRICE_ID
			AND PRICE_CODE = p_PRICE_CODE
			AND PRICE_DATE = p_PRICE_DATE
			AND AS_OF_DATE <= p_AS_OF_DATE;
	END IF;

	RETURN v_AS_OF_DATE;

	EXCEPTION
		WHEN NO_DATA_FOUND THEN
			RETURN v_AS_OF_DATE;
		WHEN OTHERS THEN
			RAISE;

END MARKET_PRICE_AS_OF_DATE;
/


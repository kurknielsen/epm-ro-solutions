CREATE OR REPLACE FUNCTION TEMPLATE_BEGIN_DATE
	(
	v_TEMPLATE_ID NUMBER
	)
	RETURN DATE IS
--Revision: $Revision: 1.18 $
-- Answer the minimum season begin date associated with the template
v_BEGIN_DATE DATE;
BEGIN
	SELECT MIN(BEGIN_DATE)
		   INTO v_BEGIN_DATE
		   		FROM SEASON A, TEMPLATE_SEASON_DAY_NAME B
				WHERE A.SEASON_ID = B.SEASON_ID
				AND   B.TEMPLATE_ID = v_TEMPLATE_ID;
	IF SQL%NOTFOUND THEN
	   v_BEGIN_DATE := SYSDATE;
	END IF;
	RETURN v_BEGIN_DATE;
END TEMPLATE_BEGIN_DATE;
/

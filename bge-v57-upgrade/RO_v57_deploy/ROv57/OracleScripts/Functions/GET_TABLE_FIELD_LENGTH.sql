-- This function returns the version of the current schema

CREATE OR REPLACE FUNCTION GET_TABLE_FIELD_LENGTH (p_TABLE_NAME IN VARCHAR2, p_COLUMN_NAME IN VARCHAR2)
RETURN NUMBER AS

  v_FIELD_LENGTH NUMBER;

BEGIN
  SELECT MIN(UTC.DATA_LENGTH)
  INTO v_FIELD_LENGTH
  FROM USER_TAB_COLS UTC
  WHERE UTC.TABLE_NAME = p_TABLE_NAME
    AND UTC.COLUMN_NAME = p_COLUMN_NAME;
    
  RETURN v_FIELD_LENGTH;
END GET_TABLE_FIELD_LENGTH;
/

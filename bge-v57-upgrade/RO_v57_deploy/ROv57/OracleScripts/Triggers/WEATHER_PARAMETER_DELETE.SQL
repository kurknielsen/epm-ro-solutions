CREATE OR REPLACE TRIGGER WEATHER_PARAMETER_DELETE
	AFTER DELETE ON WEATHER_PARAMETER
	FOR EACH ROW
BEGIN
	DELETE WEATHER_STATION_PARAMETER WHERE PARAMETER_ID = :old.PARAMETER_ID;
	DELETE STATION_PARAMETER_VALUE WHERE PARAMETER_ID = :old.PARAMETER_ID;
	DELETE NOST_WEATHER_PARAMETER_MAPPING WHERE PARAMETER_ID = :old.PARAMETER_ID;
END WEATHER_PARAMETER_DELETE;
/

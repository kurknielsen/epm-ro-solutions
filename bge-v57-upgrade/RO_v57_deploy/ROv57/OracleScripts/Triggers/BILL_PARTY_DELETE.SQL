CREATE OR REPLACE TRIGGER BILL_PARTY_DELETE
	AFTER DELETE ON BILL_PARTY
	FOR EACH ROW
BEGIN
	DELETE ACCOUNT_BILL_PARTY WHERE BILL_PARTY_ID = :old.BILL_PARTY_ID;
	DELETE METER_BILL_PARTY WHERE BILL_PARTY_ID = :old.BILL_PARTY_ID;
	DELETE ENTITY_DOMAIN_ADDRESS WHERE OWNER_ENTITY_ID = :old.BILL_PARTY_ID AND ENTITY_DOMAIN_ID = -250;
	DELETE ENTITY_DOMAIN_CONTACT WHERE OWNER_ENTITY_ID = :old.BILL_PARTY_ID AND ENTITY_DOMAIN_ID = -250;	
END BILL_PARTY_DELETE;
/

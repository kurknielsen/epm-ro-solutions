CREATE OR REPLACE TRIGGER METER_TOU_USG_FACTOR_UPDATE
	BEFORE UPDATE ON METER_TOU_USAGE_FACTOR
	FOR EACH ROW
DECLARE

BEGIN

	IF :NEW.TEMPLATE_ID != :OLD.TEMPLATE_ID THEN
		DELETE FROM METER_TOU_USAGE_FACTOR_PERIOD M
		WHERE M.TOU_USAGE_FACTOR_ID = :OLD.TOU_USAGE_FACTOR_ID;
	END IF;

END METER_TOU_USG_FACTOR_UPDATE;
/

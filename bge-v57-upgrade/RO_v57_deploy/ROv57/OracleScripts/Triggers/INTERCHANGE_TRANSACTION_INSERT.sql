CREATE OR REPLACE TRIGGER INTERCHANGE_TRANSACTION_INSERT
	AFTER INSERT ON INTERCHANGE_TRANSACTION
	FOR EACH ROW
BEGIN
	INSERT INTO IT_STATUS (TRANSACTION_ID, AS_OF_DATE, TRANSACTION_STATUS_NAME, TRANSACTION_IS_ACTIVE)
		VALUES(:new.TRANSACTION_ID, LOW_DATE, 'Active', 1);
	EXCEPTION
		WHEN DUP_VAL_ON_INDEX THEN
			NULL;
		WHEN OTHERS THEN
			RAISE;
END INTERCHANGE_TRANSACTION_INSERT;
/ 

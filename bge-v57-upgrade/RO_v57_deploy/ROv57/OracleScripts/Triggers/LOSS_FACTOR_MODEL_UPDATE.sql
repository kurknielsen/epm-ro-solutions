CREATE OR REPLACE TRIGGER LOSS_FACTOR_MODEL_UPDATE
  BEFORE UPDATE ON LOSS_FACTOR_MODEL  
  FOR EACH ROW
DECLARE
  -- local variables here
BEGIN

	IF :NEW.INTERVAL != :OLD.INTERVAL OR 
		:NEW.MODEL_TYPE != :OLD.MODEL_TYPE THEN
		DELETE FROM LOSS_FACTOR_PATTERN
		WHERE PATTERN_ID = :NEW.PATTERN_ID;
	END IF;
  
END LOSS_FACTOR_MODEL_UPDATE;
/

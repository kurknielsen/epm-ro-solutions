CREATE OR REPLACE TRIGGER ANCILLARY_SERVICE_DELETE
	AFTER DELETE ON ANCILLARY_SERVICE
	FOR EACH ROW
BEGIN
	DELETE ACCOUNT_ANCILLARY_SERVICE WHERE ANCILLARY_SERVICE_ID = :old.ANCILLARY_SERVICE_ID;
	DELETE METER_ANCILLARY_SERVICE WHERE ANCILLARY_SERVICE_ID = :old.ANCILLARY_SERVICE_ID;
END ANCILLARY_SERVICE_DELETE;
/

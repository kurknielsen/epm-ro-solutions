CREATE OR REPLACE TRIGGER ACCOUNT_TOU_USG_FACTOR_UPDATE
	BEFORE UPDATE ON ACCOUNT_TOU_USAGE_FACTOR
	FOR EACH ROW
DECLARE

BEGIN

	IF :NEW.TEMPLATE_ID != :OLD.TEMPLATE_ID THEN
		DELETE FROM ACCOUNT_TOU_USG_FACTOR_PERIOD A
		WHERE A.TOU_USAGE_FACTOR_ID = :OLD.TOU_USAGE_FACTOR_ID;
	END IF;

END ACCOUNT_TOU_USG_FACTOR_UPDATE;
/

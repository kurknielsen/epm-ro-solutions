CREATE OR REPLACE TRIGGER FORECAST_TYPE_INSERT
	BEFORE INSERT ON FORECAST_TYPE
	FOR EACH ROW

DECLARE

INSUFFICIENT_PRIVILEGES EXCEPTION;
PRAGMA EXCEPTION_INIT(INSUFFICIENT_PRIVILEGES, -1031);

BEGIN
	IF :new.FORECAST_TYPE_CODE IN ('A','B','F','P') THEN
		RAISE INSUFFICIENT_PRIVILEGES;
	END IF;
END FORECAST_TYPE_INSERT;
/

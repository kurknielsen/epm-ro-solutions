CREATE OR REPLACE TRIGGER SYSTEM_OBJECT_ATTRIB_UPDATE2
	BEFORE INSERT OR UPDATE OF ATTRIBUTE_ID ON SYSTEM_OBJECT_ATTRIBUTE
	FOR EACH ROW
BEGIN
  IF (:new.ATTRIBUTE_ID = -7 OR :old.ATTRIBUTE_ID = -7) AND SECURITY_CONTROLS.IS_SUPER_USER = 0 THEN
    ERRS.RAISE(MSGCODES.c_ERR_PRIVILEGES, 'Need to be a Super User to update.');
  END IF;
END SYSTEM_OBJECT_ATTRIB_UPDATE2;
/

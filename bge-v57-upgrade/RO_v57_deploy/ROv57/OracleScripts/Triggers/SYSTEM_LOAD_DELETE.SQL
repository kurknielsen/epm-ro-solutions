CREATE OR REPLACE TRIGGER SYSTEM_LOAD_DELETE
	AFTER DELETE ON SYSTEM_LOAD
	FOR EACH ROW
BEGIN
	DELETE SYSTEM_LOAD_AREA WHERE SYSTEM_LOAD_ID = :old.SYSTEM_LOAD_ID;
	UPDATE COMPONENT_COINCIDENT_PEAK SET A_SYSTEM_LOAD_ID = 0 WHERE A_SYSTEM_LOAD_ID = :old.SYSTEM_LOAD_ID;
	UPDATE COMPONENT_COINCIDENT_PEAK SET B_SYSTEM_LOAD_ID = 0 WHERE B_SYSTEM_LOAD_ID = :old.SYSTEM_LOAD_ID;
END SYSTEM_LOAD_DELETE;
/

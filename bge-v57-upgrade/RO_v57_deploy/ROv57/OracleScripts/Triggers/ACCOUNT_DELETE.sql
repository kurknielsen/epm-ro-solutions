CREATE OR REPLACE TRIGGER ACCOUNT_DELETE
	AFTER DELETE ON ACCOUNT
	FOR EACH ROW
BEGIN
	DELETE ACCOUNT_STATUS WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_ESP WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_EDC WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_USAGE_FACTOR WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_LOSS_FACTOR WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_PRODUCT WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_GROUP_ASSIGNMENT WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_CALENDAR WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_SERVICE_LOCATION WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_SERVICE WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE AGGREGATE_ACCOUNT_ESP WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_METER WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_ANCILLARY_SERVICE WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_UFE_PARTICIPATION WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_SCHEDULE_GROUP WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE ACCOUNT_GROWTH WHERE ACCOUNT_ID = :old.ACCOUNT_ID;
	DELETE CONTRACT_ASSIGNMENT WHERE OWNER_ENTITY_ID = :old.ACCOUNT_ID;
	DELETE TEMPORAL_ENTITY_ATTRIBUTE WHERE OWNER_ENTITY_ID = :old.ACCOUNT_ID;
	
END ACCOUNT_DELETE;
/

CREATE OR REPLACE PROCEDURE DECODE_TAG
	(
	p_TAG IN NUMBER,
	p_ALPHA_ID OUT NUMBER,
	p_BETA_ID OUT NUMBER,
	p_GAMMA_ID OUT NUMBER,
	p_DELTA_ID OUT NUMBER
	) AS
--Revision: $Revision: 1.15 $

v_RESIDUAL NUMBER := p_TAG;

BEGIN

	p_ALPHA_ID :=  v_RESIDUAL / 1000000000000000000000000000;
	v_RESIDUAL := MOD(v_RESIDUAL, 1000000000000000000000000000);
	p_BETA_ID := v_RESIDUAL / 1000000000000000000;
	v_RESIDUAL := MOD(v_RESIDUAL, 1000000000000000000);
	p_GAMMA_ID := v_RESIDUAL / 1000000000;
	p_DELTA_ID := MOD(v_RESIDUAL, 1000000000);

END DECODE_TAG;
/

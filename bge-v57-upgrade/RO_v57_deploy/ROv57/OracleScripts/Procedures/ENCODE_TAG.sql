CREATE OR REPLACE PROCEDURE ENCODE_TAG
	(
	p_ALPHA_ID IN NUMBER,
	p_BETA_ID IN NUMBER,
	p_GAMMA_ID IN NUMBER,
	p_DELTA_ID IN NUMBER,
	p_TAG OUT NUMBER
	) AS
--Revision: $Revision: 1.14 $

v_PAD CHAR(9) := '000000000';
v_ALPHA VARCHAR(24);
v_BETA VARCHAR(24);
v_GAMMA VARCHAR(24);
v_DELTA VARCHAR(24);

BEGIN

	v_ALPHA := v_PAD || TO_CHAR(p_ALPHA_ID);
	v_BETA := v_PAD || TO_CHAR(p_BETA_ID);
	v_GAMMA := v_PAD || TO_CHAR(p_GAMMA_ID);
	v_DELTA := v_PAD || TO_CHAR(p_DELTA_ID);

	p_TAG := TO_NUMBER(LTRIM(RTRIM(SUBSTR(v_ALPHA, LENGTH(v_ALPHA) - 8) || SUBSTR(v_BETA, LENGTH(v_BETA) - 8) || SUBSTR(v_GAMMA, LENGTH(v_GAMMA) - 8) || SUBSTR(v_DELTA, LENGTH(v_DELTA) - 8))));

END ENCODE_TAG;
/


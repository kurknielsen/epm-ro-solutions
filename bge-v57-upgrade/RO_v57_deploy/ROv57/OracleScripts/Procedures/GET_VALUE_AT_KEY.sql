CREATE OR REPLACE PROCEDURE GET_VALUE_AT_KEY
	(
	p_KEY IN VARCHAR,
	p_VALUE OUT VARCHAR
	) AS
--Revision: $Revision: 1.19 $

--c DEPRECATED 7/11/2001 Use GET_MODEL_VALUE_AT_KEY for current procedure.

BEGIN

	p_VALUE := VALUE_AT_KEY(p_KEY);

	EXCEPTION
		WHEN OTHERS THEN
			RAISE;

END GET_VALUE_AT_KEY;
/


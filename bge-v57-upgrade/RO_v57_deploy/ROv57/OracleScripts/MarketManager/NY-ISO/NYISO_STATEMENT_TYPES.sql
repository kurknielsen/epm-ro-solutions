DECLARE	
	v_ID NUMBER(9);
	PROCEDURE PUT_STATEMENT_TYPE(
       p_STATEMENT_TYPE_NAME IN VARCHAR2,
       p_STATEMENT_TYPE_ORDER IN NUMBER
	) IS
	v_STATEMENT_TYPE_ID NUMBER;
	BEGIN
		-- Statement Type
		BEGIN
			v_STATEMENT_TYPE_ID := EI.GET_ID_FROM_NAME(p_STATEMENT_TYPE_NAME, EC.ED_STATEMENT_TYPE);
		EXCEPTION WHEN MSGCODES.e_ERR_NO_SUCH_ENTRY THEN
			IO.PUT_STATEMENT_TYPE(v_STATEMENT_TYPE_ID, p_STATEMENT_TYPE_NAME, p_STATEMENT_TYPE_NAME, p_STATEMENT_TYPE_NAME, 0, p_STATEMENT_TYPE_ORDER);
		END;
		
		-- Set the corresponding values for the 'NYISO' temporal attribute
		RO.PUT_ENTITY_ATTRIBUTE(p_ATTRIBUTE_NAME   =>  'NYISO',
							 p_ENTITY_DOMAIN_ID => EC.ED_STATEMENT_TYPE,
							 p_OWNER_ENTITY_ID  => v_STATEMENT_TYPE_ID,
							 p_ATTRIBUTE_TYPE   => 'String',
							 p_ATTRIBUTE_VAL    => p_STATEMENT_TYPE_NAME,
							 p_begin_date => DATE'2000-01-01',
							 p_end_date => NULL);
	END;
BEGIN
	-- Add 'NYISO' and 'NYISO-DSS' temporal entity attribute
  	ID.ID_FOR_ENTITY_ATTRIBUTE('NYISO',EC.ED_STATEMENT_TYPE,'String',TRUE,v_ID);
  	ID.ID_FOR_ENTITY_ATTRIBUTE('NYISO-DSS',EC.ED_INTERCHANGE_CONTRACT,'String',TRUE,v_ID);

	-- Now add new Statement Type for NY-ISO and update the temporal attribute
	PUT_STATEMENT_TYPE('Uninvoiced', 1);
	PUT_STATEMENT_TYPE('Initial', 2);
	PUT_STATEMENT_TYPE('4 Month', 3);
	PUT_STATEMENT_TYPE('12 Month', 4);
END;

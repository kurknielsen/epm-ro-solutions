DECLARE
	v_DISPLAY_ORDER BINARY_INTEGER := 0;
	v_TG TRANSACTION_TRAIT_GROUP%ROWTYPE;
	v_STATUS NUMBER;
		
	PROCEDURE PUT_TRAIT
		(
		p_TRAIT_GROUP_ID IN NUMBER,
		p_TRAIT_GROUP_NAME IN VARCHAR2,
		p_TRAIT_INTERVAL IN VARCHAR2,
		p_TRAIT_CATEGORY IN VARCHAR2,
		p_DATA_TYPE IN VARCHAR2,
		p_COMBO_LIST IN VARCHAR2 := NULL,
		p_FORMAT IN VARCHAR2 := NULL,
        p_EDIT_MASK IN VARCHAR2 := NULL,
		p_AFTER_EDIT_VALIDATION IN VARCHAR2 := NULL
		) AS
	BEGIN
		v_DISPLAY_ORDER := v_DISPLAY_ORDER + 1;
	
		-- Set common TG properties.
		v_TG.TRAIT_GROUP_INTERVAL := p_TRAIT_INTERVAL;
		v_TG.IS_SERIES := 0;
		v_TG.IS_SPARSE := 1;
		v_TG.IS_STATEMENT_TYPE_SPECIFIC := 0;
		v_TG.DEFAULT_NUMBER_OF_SETS := 0;

		v_TG.TRAIT_GROUP_ID := p_TRAIT_GROUP_ID;
		v_TG.TRAIT_GROUP_NAME := 'OASIS ' || p_TRAIT_GROUP_NAME;
		v_TG.TRAIT_GROUP_DESC := v_TG.TRAIT_GROUP_NAME;
		v_TG.DISPLAY_NAME := 'OASIS';
		v_TG.DISPLAY_ORDER := v_DISPLAY_ORDER;
		v_TG.TRAIT_CATEGORY := p_TRAIT_CATEGORY;
		v_TG.TRAIT_GROUP_TYPE := 'Sparse';
		
		TG.UPSERT_TRAIT_GROUP(v_TG);
        EM.PUT_TRANSACTION_TRAIT(v_TG.TRAIT_GROUP_ID, 1, 0, p_TRAIT_GROUP_NAME, 1, p_DATA_TYPE, p_FORMAT, p_COMBO_LIST, p_EDIT_MASK, p_AFTER_EDIT_VALIDATION, v_STATUS);
	EXCEPTION
		WHEN OTHERS THEN
			UT.DEBUG_TRACE('TRAIT_GROUP:DID NOT SUCCESSFULLY CREATE ' || v_TG.TRAIT_GROUP_NAME || '.  STATUS=' || v_STATUS);
	END PUT_TRAIT;

BEGIN -- MAIN

	v_TG.TRAIT_GROUP_ALIAS := '?';
	v_TG.SC_ID := ID.ID_FOR_SC('OASIS');

	PUT_TRAIT(MM_OASIS_UTIL.g_TG_OFFER_PRICE, 'Offer Price', '<Offer>', '%', 'Number', NULL, 'Currency');
	PUT_TRAIT(MM_OASIS_UTIL.g_TG_CAP_REQUESTED, 'Capacity Requested', '<Offer>', '%', 'Number');
	PUT_TRAIT(MM_OASIS_UTIL.g_TG_CAP_GRANTED, 'Capacity Granted', '<Offer>', '%', 'Number');
	PUT_TRAIT(MM_OASIS_UTIL.g_TG_PRECONFIRMED, 'Preconfirmed', '<Offer>', '%', 'String', 'Y|N');
	PUT_TRAIT(MM_OASIS_UTIL.g_TG_CUST_COMMENTS, 'Customer Comments', '<Offer>', '%', 'String');
	PUT_TRAIT(MM_OASIS_UTIL.g_TG_STATUS_COMMENTS, 'Status Comments', '<Offer>', '%', 'String');
	PUT_TRAIT(MM_OASIS_UTIL.g_TG_SELLER_COMMENTS, 'Seller Comments', '<Offer>', '%', 'String');
	
	COMMIT;
END;

DECLARE
	v_PJM_SET_ID NUMBER(9);

	PROCEDURE ADD_HOLIDAY_TO_SET
		(
		p_HOLIDAY_NAME IN VARCHAR2
		) AS
		v_HOLIDAY_ID NUMBER(9);
	BEGIN
		
		ID.ID_FOR_HOLIDAY(p_HOLIDAY_NAME, v_HOLIDAY_ID);	
		INSERT INTO HOLIDAY_SCHEDULE VALUES(v_PJM_SET_ID, v_HOLIDAY_ID, SYSDATE);
	EXCEPTION
		WHEN OTHERS THEN NULL;
	END ADD_HOLIDAY_TO_SET;
BEGIN
	
	BEGIN
		SELECT HOLIDAY_SET_ID INTO v_PJM_SET_ID FROM HOLIDAY_SET WHERE HOLIDAY_SET_NAME = 'NERC';
	EXCEPTION 
		WHEN OTHERS THEN
			IO.PUT_HOLIDAY_SET(v_PJM_SET_ID, 'NERC', 'NERC', 'NERC Holidays', 0);
	END;

	SP.CREATE_US_HOLIDAYS(2000,2020);
	
	ADD_HOLIDAY_TO_SET('New Years Day');
	ADD_HOLIDAY_TO_SET('Memorial Day');
	ADD_HOLIDAY_TO_SET('Independence Day');
	ADD_HOLIDAY_TO_SET('Labor Day');
	ADD_HOLIDAY_TO_SET('Thanksgiving');
	ADD_HOLIDAY_TO_SET('Christmas');
END;

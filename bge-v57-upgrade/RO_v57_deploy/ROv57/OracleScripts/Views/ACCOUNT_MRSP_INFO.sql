CREATE OR REPLACE VIEW ACCOUNT_MRSP_INFO ( ACCOUNT_ID, 
MRSP_ID, BEGIN_DATE, END_DATE, MRSP_ACCOUNT_NUMBER, 
MRSP_NAME, ENTRY_DATE, CALENDAR_ID, CALENDAR_NAME
 ) AS 
SELECT A.ACCOUNT_ID, A.MRSP_ID, A.BEGIN_DATE, A.END_DATE, A.MRSP_ACCOUNT_NUMBER,
M.MRSP_NAME, A.ENTRY_DATE, C.CALENDAR_ID, C.CALENDAR_NAME
FROM ACCOUNT_MRSP A, METER_READING_SERVICE_PROVIDER M, CALENDAR C
WHERE A.MRSP_ID = M.MRSP_ID AND A.METER_CYCLE_CALENDAR_ID = C.CALENDAR_ID(+);
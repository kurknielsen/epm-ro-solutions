CREATE OR REPLACE VIEW CONTACT_AND_PHONE_INFO ( ENTITY_DOMAIN_ID, 
OWNER_ENTITY_ID, CATEGORY_ID, CATEGORY_NAME, CONTACT_NAME, 
CONTACT_ID, EMAIL_ADDRESS, PHONE_TYPE, PHONE_NUMBER, 
ENTRY_DATE, CONTACT_ALIAS, CONTACT_DESC, FIRST_NAME, 
MIDDLE_NAME, LAST_NAME, SALUTATION ) AS SELECT E.ENTITY_DOMAIN_ID, E.OWNER_ENTITY_ID, E.CATEGORY_ID, T.CATEGORY_NAME,
	C.CONTACT_NAME, C.CONTACT_ID, C.EMAIL_ADDRESS, P.PHONE_TYPE, P.PHONE_NUMBER,
	P.ENTRY_DATE, C.CONTACT_ALIAS, C.CONTACT_DESC, C.FIRST_NAME, C.MIDDLE_NAME,
	C.LAST_NAME, C.SALUTATION
FROM ENTITY_DOMAIN_CONTACT E, CONTACT C, CATEGORY T, PHONE_NUMBER P
WHERE C.CONTACT_ID = E.CONTACT_ID AND T.CATEGORY_ID(+) = E.CATEGORY_ID AND C.CONTACT_ID = P.CONTACT_ID(+);

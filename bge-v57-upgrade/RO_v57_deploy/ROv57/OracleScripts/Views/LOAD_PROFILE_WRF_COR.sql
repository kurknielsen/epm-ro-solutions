CREATE OR REPLACE VIEW LOAD_PROFILE_WRF_COR ( PROFILE_ID, 
WRF_ID, WRF_HOUR, COEFF_0, COEFF_1, COEFF_2, COEFF_3, COEFF_4, COEFF_5,
R_SQUARED, TSTAT_0, TSTAT_1, TSTAT_2, TSTAT_3, TSTAT_4, TSTAT_5,
TSTAT_CRITICAL, WRF_MIN, WRF_MAX, AS_OF_DATE, WRF_LINE_NBR
 ) AS 
SELECT B.PROFILE_ID, A.WRF_ID, A.WRF_HOUR, A.COEFF_0, A.COEFF_1, A.COEFF_2, A.COEFF_3, A.COEFF_4, A.COEFF_5,
A.R_SQUARED, A.TSTAT_0, A.TSTAT_1, A.TSTAT_2, A.TSTAT_3, A.TSTAT_4, A.TSTAT_5, A.TSTAT_CRITICAL,
B.SEGMENT_MIN, B.SEGMENT_MAX, B.AS_OF_DATE, B.WRF_LINE_NBR
FROM LOAD_PROFILE_WRF_LINE A,
LOAD_PROFILE_WRF B
WHERE A.WRF_ID = B.WRF_ID
	AND B.WRF_LINE_NBR < 4;
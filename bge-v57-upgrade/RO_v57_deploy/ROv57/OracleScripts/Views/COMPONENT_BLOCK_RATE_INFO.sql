CREATE OR REPLACE VIEW COMPONENT_BLOCK_RATE_INFO ( COMPONENT_ID, 
BEGIN_DATE, END_DATE, BLOCK_ID, BLOCK_MIN, 
BLOCK_MAX, CHARGE_MIN, RATE, ENTRY_DATE
 ) AS SELECT B.COMPONENT_ID, B.BEGIN_DATE, B.END_DATE,
	   -1, B.BLOCK_MIN, B.BLOCK_MAX, B.CHARGE_MIN, B.RATE, B.ENTRY_DATE  
FROM COMPONENT_BLOCK_RATE B;

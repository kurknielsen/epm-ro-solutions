CREATE OR REPLACE VIEW ACCOUNT_EDC_INFO ( ACCOUNT_ID, 
EDC_ID, BEGIN_DATE, END_DATE, EDC_ACCOUNT_NUMBER, 
EDC_RATE_CLASS, EDC_STRATA, ENTRY_DATE, EDC_NAME
 ) AS SELECT A.ACCOUNT_ID, A.EDC_ID, A.BEGIN_DATE, A.END_DATE, A.EDC_ACCOUNT_NUMBER, 
A.EDC_RATE_CLASS, A.EDC_STRATA, A.ENTRY_DATE, E.EDC_NAME 
FROM ACCOUNT_EDC A, ENERGY_DISTRIBUTION_COMPANY E 
WHERE A.EDC_ID = E.EDC_ID;


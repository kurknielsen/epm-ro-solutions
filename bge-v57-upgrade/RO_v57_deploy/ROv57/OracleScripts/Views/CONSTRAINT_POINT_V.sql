CREATE OR REPLACE VIEW CONSTRAINT_POINT_V
(SERVICE_POINT_ID, BEGIN_DATE, END_DATE, CONSTRAINT_POINT_ID)
AS
SELECT OWNER_ENTITY_ID,BEGIN_DATE,END_DATE,NVL(SERVICE_POINT_ID,0)
FROM ENTITY_ATTRIBUTE A, TEMPORAL_ENTITY_ATTRIBUTE T, SERVICE_POINT SP
WHERE A.ATTRIBUTE_NAME = 'Constraint Point'
	AND A.ENTITY_DOMAIN_ID = -210 --EC.ED_SERVICE_POINT
	AND T.ATTRIBUTE_ID = A.ATTRIBUTE_ID
	AND SP.SERVICE_POINT_NAME(+) = T.ATTRIBUTE_VAL;
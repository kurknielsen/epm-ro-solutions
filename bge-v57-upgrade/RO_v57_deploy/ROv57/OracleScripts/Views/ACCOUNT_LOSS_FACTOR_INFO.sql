CREATE OR REPLACE VIEW ACCOUNT_LOSS_FACTOR_INFO ( ACCOUNT_ID, 
LOSS_FACTOR_ID, BEGIN_DATE, END_DATE, ENTRY_DATE, 
LOSS_FACTOR_NAME ) AS 
SELECT A.ACCOUNT_ID, A.LOSS_FACTOR_ID, A.BEGIN_DATE, A.END_DATE, A.ENTRY_DATE, L.LOSS_FACTOR_NAME
FROM ACCOUNT_LOSS_FACTOR A, LOSS_FACTOR L
WHERE A.LOSS_FACTOR_ID = L.LOSS_FACTOR_ID;


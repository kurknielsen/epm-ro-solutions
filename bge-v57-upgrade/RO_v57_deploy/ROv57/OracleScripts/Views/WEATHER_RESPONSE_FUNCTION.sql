CREATE OR REPLACE VIEW WEATHER_RESPONSE_FUNCTION ( PROFILE_ID, 
WRF_LINE_NBR, AS_OF_DATE, WRF_TEMP_MIN, WRF_TEMP_MAX, 
WRF_HOUR, COEFF_0, COEFF_1, COEFF_2, 
R_SQUARED, TSTAT_0, TSTAT_1, TSTAT_2, 
TSTAT_CRITICAL ) AS SELECT A.PROFILE_ID, 
    A.WRF_LINE_NBR, 
	A.AS_OF_DATE, 
	A.SEGMENT_MIN, 
	A.SEGMENT_MAX, 
	B.WRF_HOUR, 
	B.COEFF_0, 
	B.COEFF_1, 
	B.COEFF_2, 
	B.R_SQUARED, 
	B.TSTAT_0, 
	B.TSTAT_1, 
	B.TSTAT_2, 
	B.TSTAT_CRITICAL 
FROM LOAD_PROFILE_WRF A, 
    LOAD_PROFILE_WRF_LINE B 
WHERE A.WRF_ID = B.WRF_ID;

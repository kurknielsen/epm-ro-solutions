CREATE OR REPLACE VIEW PHYSICAL_POSITION_TP ( NAME, 
ID, SCHEDULE_TYPE, SCHEDULE_DATE ) AS SELECT DISTINCT D.TP_NAME "NAME", 
		D.TP_ID "ID", 
		B.SCHEDULE_TYPE, 
		TRUNC(B.SCHEDULE_DATE) "SCHEDULE_DATE"
	FROM INTERCHANGE_TRANSACTION A, 
		IT_SCHEDULE B, 
		SERVICE_POINT C, 
	 	TRANSMISSION_PROVIDER D 
	WHERE A.TRANSACTION_ID = B.TRANSACTION_ID 
		AND A.POD_ID = C.SERVICE_POINT_ID 
		AND C.TP_ID > 0 
		AND C.TP_ID = D.TP_ID;


CREATE OR REPLACE VIEW LOAD_PROFILE_RETAIL_ACCOUNT ( PROFILE_ID, 
POINT_INDEX, POINT_VAL, POINT_DATE, AS_OF_DATE, 
PROFILE_ACCOUNT_REF, PROFILE_INTERVAL, SOURCE_VERSION ) AS 
SELECT A.PROFILE_ID, A.POINT_INDEX, A.POINT_VAL, A.POINT_DATE, A.AS_OF_DATE, 
B.PROFILE_ACCOUNT_REF, B.PROFILE_INTERVAL, B.PROFILE_SOURCE_VERSION 
FROM LOAD_PROFILE_POINT A, 
LOAD_PROFILE B 
WHERE A.PROFILE_ID = B.PROFILE_ID;
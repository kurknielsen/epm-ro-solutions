CREATE OR REPLACE VIEW EXPORT_FORECAST_PPL (ACCOUNT_ID, SERVICE_ID, THE_DATE, THE_VAL, EDC_ID, ESP_ID, EDC_ACCOUNT_NUMBER, THE_AS_OF_DATE ) AS
	SELECT B.ACCOUNT_ID, C.SERVICE_ID, D.LOAD_DATE "THE_DATE", D.LOAD_VAL "THE_VAL", A.EDC_ID, A.ESP_ID, EDC_ACCOUNT_NUMBER, C.AS_OF_DATE "THE_AS_OF_DATE"
	FROM PROVIDER_SERVICE A, ACCOUNT_SERVICE B, SERVICE C, SERVICE_LOAD D, ACCOUNT_EDC E
	WHERE C.ACCOUNT_SERVICE_ID = B.ACCOUNT_SERVICE_ID
		  AND C.PROVIDER_SERVICE_ID = A.PROVIDER_SERVICE_ID
		  AND D.SERVICE_ID = C.SERVICE_ID
		  AND D.SERVICE_CODE = 'F'
		  AND D.LOAD_CODE = 0
		  AND D.LOAD_DATE >= E.BEGIN_DATE
		  AND (D.LOAD_DATE <= E.END_DATE OR E.END_DATE IS NULL);

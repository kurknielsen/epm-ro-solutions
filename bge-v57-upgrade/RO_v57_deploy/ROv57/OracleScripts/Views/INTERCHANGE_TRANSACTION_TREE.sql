CREATE OR REPLACE VIEW INTERCHANGE_TRANSACTION_TREE ( TRANSACTION_TYPE, 
TRANSACTION_NAME, TRANSACTION_ID, COMMODITY_TYPE, BEGIN_DATE, 
END_DATE ) AS
SELECT RTRIM(TRANSACTION_TYPE) || 's',RTRIM(TRANSACTION_NAME),
	TRANSACTION_ID,
	COMMODITY_TYPE,
	BEGIN_DATE,
	END_DATE
FROM INTERCHANGE_TRANSACTION A,
	IT_COMMODITY B
WHERE A.COMMODITY_ID = B.COMMODITY_ID;


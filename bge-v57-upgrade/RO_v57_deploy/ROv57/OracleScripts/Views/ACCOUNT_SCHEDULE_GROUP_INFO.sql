CREATE OR REPLACE VIEW ACCOUNT_SCHEDULE_GROUP_INFO ( ACCOUNT_ID, 
SCHEDULE_GROUP_ID, BEGIN_DATE, END_DATE, ENTRY_DATE, 
SCHEDULE_GROUP_NAME ) AS SELECT A.ACCOUNT_ID, A.SCHEDULE_GROUP_ID, A.BEGIN_DATE, A.END_DATE, A.ENTRY_DATE, S.SCHEDULE_GROUP_NAME 
FROM ACCOUNT_SCHEDULE_GROUP A, SCHEDULE_GROUP S 
WHERE A.SCHEDULE_GROUP_ID = S.SCHEDULE_GROUP_ID;


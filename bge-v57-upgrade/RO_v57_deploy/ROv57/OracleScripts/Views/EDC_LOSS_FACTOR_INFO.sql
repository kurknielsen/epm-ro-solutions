CREATE OR REPLACE VIEW EDC_LOSS_FACTOR_INFO ( EDC_ID, 
LOSS_FACTOR_ID, BEGIN_DATE, END_DATE, ENTRY_DATE, 
LOSS_FACTOR_NAME ) AS SELECT E.EDC_ID, E.LOSS_FACTOR_ID, E.BEGIN_DATE, E.END_DATE, E.ENTRY_DATE, L.LOSS_FACTOR_NAME
FROM EDC_LOSS_FACTOR E, LOSS_FACTOR L
WHERE E.LOSS_FACTOR_ID = L.LOSS_FACTOR_ID;


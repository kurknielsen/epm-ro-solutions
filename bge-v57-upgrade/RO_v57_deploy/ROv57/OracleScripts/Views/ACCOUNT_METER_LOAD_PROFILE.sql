CREATE OR REPLACE VIEW ACCOUNT_METER_LOAD_PROFILE ( ACCOUNT_NAME, 
ACCOUNT_ID, SERVICE_LOCATION_ID, METER_ID, CALENDAR_ID, 
PROFILE_ID, PROFILE_TYPE ) AS 
SELECT A.ACCOUNT_NAME,
	A.ACCOUNT_ID,
	B.SERVICE_LOCATION_ID,
	C.METER_ID,
	D.CALENDAR_ID,
	E.PROFILE_ID,
	F.PROFILE_TYPE
FROM ACCOUNT A,
	ACCOUNT_SERVICE_LOCATION B,
	SERVICE_LOCATION_METER C,
	METER_CALENDAR D,
	CALENDAR_PROFILE E,
	LOAD_PROFILE F
WHERE A.ACCOUNT_ID = B.ACCOUNT_ID
	AND B.SERVICE_LOCATION_ID = C.SERVICE_LOCATION_ID
	AND C.METER_ID = D.METER_ID
	AND D.CALENDAR_ID = E.CALENDAR_ID(+)
	AND E.PROFILE_ID = F.PROFILE_ID(+);

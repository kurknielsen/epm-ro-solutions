CREATE OR REPLACE VIEW PHYSICAL_POSITION_EDC ( NAME, 
ID, SCHEDULE_DATE ) AS SELECT DISTINCT D.EDC_NAME "NAME",
		D.EDC_ID "ID",
		TRUNC(B.SCHEDULE_DATE) "SCHEDULE_DATE"
	FROM INTERCHANGE_TRANSACTION A,
		IT_SCHEDULE B,
		SERVICE_POINT C,
	 	ENERGY_DISTRIBUTION_COMPANY D
	WHERE A.TRANSACTION_ID = B.TRANSACTION_ID
		AND A.POD_ID = C.SERVICE_POINT_ID
		AND C.EDC_ID > 0
		AND C.EDC_ID = D.EDC_ID;


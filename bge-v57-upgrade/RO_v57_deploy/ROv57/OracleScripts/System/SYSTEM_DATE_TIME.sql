--THIS SCRIPT POPULATES THE SYSTEM_DATE_TIME AND HELPER TABLES.
DECLARE
	v_TIME_ZONES	STRING_COLLECTION;
	v_BEGIN_DATE	DATE;
	v_END_DATE	DATE;
	
	v_PROCESS_ID	NUMBER;
	v_PROCESS_STATUS NUMBER;
	v_MESSAGE	VARCHAR2(4000);
BEGIN
	-- set interval setting in the system dictionary
	PUT_DICTIONARY_VALUE('Minimum Interval', '&2', CONSTANTS.GLOBAL_MODEL, 'System', 'System-Date-Time');
	
	UT.STRING_COLLECTION_FROM_STRING('&1',',',v_TIME_ZONES);
	v_BEGIN_DATE := DATE '&3-01-01';
	v_END_DATE := DATE '&4-12-31';
	
	POPULATE_SYSTEM_DATE_TIME.RUN(v_BEGIN_DATE, v_END_DATE, v_TIME_ZONES, NULL, v_PROCESS_ID, v_PROCESS_STATUS, v_MESSAGE);
END;
/

DECLARE
v_PROXY_DAY_METHOD_ID PLS_INTEGER;
v_SYSTEM_LOAD_ID PLS_INTEGER;
v_TEMPLATE_ID PLS_INTEGER;
v_HOLIDAY_SET_ID PLS_INTEGER;
BEGIN
   SECURITY_CONTROLS.SET_CURRENT_USER(SECURITY_CONTROLS.c_SUSER_SYSTEM);
   SELECT MAX(PROXY_DAY_METHOD_ID) INTO v_PROXY_DAY_METHOD_ID FROM PROXY_DAY_METHOD WHERE PROXY_DAY_METHOD_NAME = 'BGE Default Method';
   SELECT MAX(SYSTEM_LOAD_ID) INTO v_SYSTEM_LOAD_ID FROM SYSTEM_LOAD WHERE SYSTEM_LOAD_NAME = 'BGE Load'; 
   ASSERT(v_SYSTEM_LOAD_ID IS NOT NULL, 'System Load "BGE Load" Is Not Defined');
   SELECT MAX(TEMPLATE_ID) INTO v_TEMPLATE_ID FROM TEMPLATE WHERE TEMPLATE_NAME = 'Weekday/Sat/Sun+Hol'; 
   ASSERT(v_TEMPLATE_ID IS NOT NULL, 'Template "Weekday/Sat/Sun+Hol" Is Not Defined');
   SELECT MAX(HOLIDAY_SET_ID) INTO v_HOLIDAY_SET_ID FROM HOLIDAY_SET WHERE HOLIDAY_SET_NAME = 'BGE Holiday Set'; 
   ASSERT(v_HOLIDAY_SET_ID IS NOT NULL, 'Holiday Set "BGE Holiday Set" Is Not Defined');
   IO.PUT_PROXY_DAY_METHOD(
      o_OID => v_PROXY_DAY_METHOD_ID,
      p_PROXY_DAY_METHOD_NAME     => 'BGE Default Method',
      p_PROXY_DAY_METHOD_ALIAS    => 'BGE Default Method',
      p_PROXY_DAY_METHOD_DESC     => 'BGE Default Method',
      p_PROXY_DAY_METHOD_ID       => v_PROXY_DAY_METHOD_ID,
      p_TEMPLATE_ID               => v_TEMPLATE_ID,
      p_COMPARATIVE_VALUE         => 'S',
      p_STATION_ID                => NULL,
      p_PARAMETER_ID              => NULL,
      p_SYSTEM_LOAD_ID            => v_SYSTEM_LOAD_ID,
      p_LOOKUP_TIME_HORIZON       => 30,
      p_TIME_HORIZON_SHIFT        => 1,
      p_LOOKUP_CANDIDATE_LIMIT    => 5,
      p_CANDIDATE_DELTA_THRESHOLD => 0,
      p_HOLIDAY_SET_ID            => v_HOLIDAY_SET_ID);
END;
/

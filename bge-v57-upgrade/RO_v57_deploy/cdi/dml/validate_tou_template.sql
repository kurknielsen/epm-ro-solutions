DECLARE
v_TEMPLATE_ID PLS_INTEGER;
v_MESSAGE VARCHAR2(1000);
BEGIN
   SECURITY_CONTROLS.SET_CURRENT_USER(SECURITY_CONTROLS.c_SUSER_SYSTEM);
   SELECT MAX(TEMPLATE_ID) INTO v_TEMPLATE_ID FROM TEMPLATE WHERE TEMPLATE_NAME = 'Anytime TOU Template';
   IF v_TEMPLATE_ID IS NOT NULL THEN
      EM.VALIDATE_TOU_TEMPLATE(p_TEMPLATE_ID => v_TEMPLATE_ID, p_MESSAGE => v_MESSAGE);
      DBMS_OUTPUT.PUT_LINE('VALIDATE_TOU_TEMPLATE Message: ' || v_MESSAGE);
   ELSE
      DBMS_OUTPUT.PUT_LINE('VALIDATE_TOU_TEMPLATE "Anytime TOU Template" Is Not Defined.');
   END IF;
   SELECT MAX(TEMPLATE_ID) INTO v_TEMPLATE_ID FROM TEMPLATE WHERE TEMPLATE_NAME = 'Weekday/Sat/Sun+Hol';
   IF v_TEMPLATE_ID IS NOT NULL THEN
      EM.VALIDATE_TOU_TEMPLATE(p_TEMPLATE_ID => v_TEMPLATE_ID, p_MESSAGE => v_MESSAGE);
      DBMS_OUTPUT.PUT_LINE('VALIDATE_TOU_TEMPLATE Message: ' || v_MESSAGE);
   ELSE
      DBMS_OUTPUT.PUT_LINE('VALIDATE_TOU_TEMPLATE "Weekday/Sat/Sun+Hol" Is Not Defined.');
   END IF;
   COMMIT;
END;
/

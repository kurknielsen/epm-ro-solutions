CREATE GLOBAL TEMPORARY TABLE CDI_MONTHLY_USAGE_FACTOR
(
  BILL_ACCOUNT    NUMBER,
  SERVICE_POINT   NUMBER,
  BEGIN_DATE      DATE,
  END_DATE        DATE,
  EDC_ID          NUMBER(9),
  STATION_ID      NUMBER(9),
  CALENDAR_ID     NUMBER(9),
  BILLED_USAGE    NUMBER(14,4),
  PROFILED_USAGE  NUMBER(14,4),
  PROFILED_DAYS   NUMBER(6),
  USAGE_DAYS      NUMBER(6),
  USAGE_FACTOR    NUMBER(14,6),
  BGE_TIMESTAMP   TIMESTAMP(6)
) ON COMMIT PRESERVE ROWS;

CREATE INDEX IX_CDI_MONTHLY_USAGE_FACTOR ON CDI_MONTHLY_USAGE_FACTOR(BILL_ACCOUNT, SERVICE_POINT, BEGIN_DATE, END_DATE);

EXEC CDI_DROP_OBJECT('MEX_MESSAGE', 'TABLE');

CREATE TABLE MEX_MESSAGE
(
  MESSAGE_ID           NUMBER(9) NOT NULL,
  MESSAGE_DATE         DATE,
  MARKET_OPERATOR      VARCHAR2(32 BYTE),
  MESSAGE_REALM        VARCHAR2(16 BYTE),
  MESSAGE_PRIORITY     NUMBER(9),
  EFFECTIVE_DATE       DATE,
  TERMINATION_DATE     DATE,
  MESSAGE_SOURCE       VARCHAR2(64 BYTE),
  MESSAGE_DESTINATION  VARCHAR2(64 BYTE),
  MESSAGE_TEXT         VARCHAR2(4000 BYTE),
  ENTRY_DATE           DATE,
  CONSTRAINT PK_MEX_MESSAGE PRIMARY KEY(MESSAGE_ID)
) TABLESPACE NERO_DATA;

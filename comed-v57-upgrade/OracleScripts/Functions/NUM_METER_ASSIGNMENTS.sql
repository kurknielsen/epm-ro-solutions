CREATE OR REPLACE FUNCTION NUM_METER_ASSIGNMENTS
	(
	p_METER_ID IN NUMBER
	) RETURN NUMBER IS
--Revision: $Revision: 1.17 $

v_COUNT NUMBER;

BEGIN

	BEGIN
		SELECT COUNT(*)
		INTO v_COUNT
		FROM SERVICE_LOCATION_METER
		WHERE METER_ID = p_METER_ID;
	EXCEPTION
		WHEN NO_DATA_FOUND THEN
			v_COUNT := 0;
	END;

	RETURN v_COUNT;

END NUM_METER_ASSIGNMENTS;
/


CREATE OR REPLACE FUNCTION DST_TIME_ZONE
	(
	p_TIME_ZONE IN VARCHAR
	) RETURN VARCHAR IS
--Revision: $Revision: 1.20 $

BEGIN

	IF TRIM(p_TIME_ZONE) = 'GMT' THEN
		RETURN 'GMT';
   ELSIF p_TIME_ZONE = 'CES' OR p_TIME_ZONE = 'CET' THEN
    RETURN 'CES';
 	ELSE
		RETURN UPPER(SUBSTR(p_TIME_ZONE,1,1)) || 'DT';
	END IF;

END DST_TIME_ZONE;
/

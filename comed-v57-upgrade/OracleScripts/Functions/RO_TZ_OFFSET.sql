CREATE OR REPLACE FUNCTION RO_TZ_OFFSET(p_TIME_ZONE IN VARCHAR2) RETURN VARCHAR2 AS
  v_OFFSET SYSTEM_TIME_ZONE.STANDARD_TIME_ZONE_OFFSET%TYPE;
BEGIN
  
  SELECT Z.STANDARD_TIME_ZONE_OFFSET INTO v_OFFSET
	FROM SYSTEM_TIME_ZONE Z
	WHERE TIME_ZONE = p_TIME_ZONE;  

  RETURN v_OFFSET;
END RO_TZ_OFFSET;
/

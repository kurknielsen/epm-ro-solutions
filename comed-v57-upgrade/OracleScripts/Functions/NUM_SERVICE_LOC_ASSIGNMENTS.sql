CREATE OR REPLACE FUNCTION NUM_SERVICE_LOC_ASSIGNMENTS
	(
	p_SERVICE_LOCATION_ID IN NUMBER
	) RETURN NUMBER IS
--Revision: $Revision: 1.17 $

v_COUNT NUMBER;

BEGIN

	BEGIN
		SELECT COUNT(*)
		INTO v_COUNT
		FROM ACCOUNT_SERVICE_LOCATION
		WHERE SERVICE_LOCATION_ID = p_SERVICE_LOCATION_ID;
	EXCEPTION
		WHEN NO_DATA_FOUND THEN
			v_COUNT := 0;
	END;

	RETURN v_COUNT;

END NUM_SERVICE_LOC_ASSIGNMENTS;
/


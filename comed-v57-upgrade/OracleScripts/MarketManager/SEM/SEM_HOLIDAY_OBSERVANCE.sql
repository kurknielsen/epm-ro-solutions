DECLARE
	PROCEDURE ADD_HOLIDAY_OBSERVANCE(p_HOLIDAY_NAME IN VARCHAR2, p_HOLIDAY_DATE IN DATE, p_HOLIDAY_YEAR IN NUMBER := 2008) AS
		v_HOLIDAY_ID NUMBER(9);
	BEGIN
		ID.ID_FOR_HOLIDAY(p_HOLIDAY_NAME, v_HOLIDAY_ID);
		IF v_HOLIDAY_ID > 0 THEN	
			SP.PUT_HOLIDAY_OBSERVANCE(v_HOLIDAY_ID, p_HOLIDAY_YEAR, p_HOLIDAY_DATE);
		END IF;
	EXCEPTION
		WHEN OTHERS THEN NULL;
	END ADD_HOLIDAY_OBSERVANCE;
BEGIN
	SECURITY_CONTROLS.SET_CURRENT_ROLES(ID_TABLE(ID_TYPE(SECURITY_CONTROLS.g_SUPER_USER_ROLE_ID)));
	
	-- 2007 holidays after market opening
	ADD_HOLIDAY_OBSERVANCE('Christmas', DATE '2007-12-25', 2007);
	ADD_HOLIDAY_OBSERVANCE('Boxing Day', DATE '2007-12-26', 2007);

	-- 2008 holidays
	ADD_HOLIDAY_OBSERVANCE('New Year''s Day', DATE '2008-01-01');
	ADD_HOLIDAY_OBSERVANCE('St. Patrick''s Day', DATE '2008-03-17');
	ADD_HOLIDAY_OBSERVANCE('Good Friday', DATE '2008-03-21');
	ADD_HOLIDAY_OBSERVANCE('Easter Monday', DATE '2008-03-24');
	ADD_HOLIDAY_OBSERVANCE('May Day', DATE '2008-05-05');
	ADD_HOLIDAY_OBSERVANCE('May Bank Holiday (NI)', DATE '2008-05-26');
	ADD_HOLIDAY_OBSERVANCE('June Bank Holiday (ROI)', DATE '2008-06-02');
	ADD_HOLIDAY_OBSERVANCE('The Twelfth', DATE '2008-07-14');
	ADD_HOLIDAY_OBSERVANCE('August Bank Holiday (ROI)', DATE '2008-08-04');
	ADD_HOLIDAY_OBSERVANCE('August Bank Holiday', DATE '2008-08-25');
	ADD_HOLIDAY_OBSERVANCE('October Bank Holiday (ROI)', DATE '2008-10-27');
	ADD_HOLIDAY_OBSERVANCE('Christmas', DATE '2008-12-25');
	ADD_HOLIDAY_OBSERVANCE('Boxing Day', DATE '2008-12-26');
END;
/
COMMIT;

DECLARE

	PROCEDURE CREATE_MARKET_PRICE
		(
		p_MARKET_PRICE_NAME IN VARCHAR2,
		p_INTERVAL IN VARCHAR2,
		p_PRICE_TYPE IN VARCHAR2
		) IS
		v_MARKET_PRICE_ID NUMBER := -1;
	BEGIN
		ID.ID_FOR_MARKET_PRICE(p_MARKET_PRICE_NAME, FALSE, v_MARKET_PRICE_ID);
		IF v_MARKET_PRICE_ID <= 0 THEN
			IO.PUT_MARKET_PRICE(v_MARKET_PRICE_ID, 
				p_MARKET_PRICE_NAME, 
				NULL, 
				'Price Created on ' || UT.TRACE_DATE(SYSDATE) || ' for use with CFDs.',
				0,
				p_PRICE_TYPE,
				p_INTERVAL,
				NULL,
				0,
				NULL,
				p_MARKET_PRICE_NAME,
				0, 0, 0, 0);
		END IF;
	END;
				
BEGIN

	CREATE_MARKET_PRICE(MM_SEM_CFD_UTIL.k_PRICE_EXCH_EUR_TO_GBP, 'Day', 'Exchange Rate');
	CREATE_MARKET_PRICE(MM_SEM_CFD_UTIL.k_PRICE_EXCH_EUR_TO_USD, 'Day', 'Exchange Rate');
	CREATE_MARKET_PRICE(MM_SEM_CFD_UTIL.k_PRICE_EXCH_GBP_TO_USD, 'Day', 'Exchange Rate');
	CREATE_MARKET_PRICE(MM_SEM_CFD_UTIL.k_PRICE_NAT_GAS, 'Month', 'Commodity Price');
	CREATE_MARKET_PRICE(MM_SEM_CFD_UTIL.k_PRICE_LOW_SULPHUR_FUEL_USD, 'Month', 'Commodity Price');
	CREATE_MARKET_PRICE(MM_SEM_CFD_UTIL.k_PRICE_GASOIL_FRONTLINE_USD, 'Month', 'Commodity Price');
	CREATE_MARKET_PRICE(MM_SEM_CFD_UTIL.k_PRICE_GASOIL_CARGO_USD, 'Month', 'Commodity Price');
	CREATE_MARKET_PRICE(MM_SEM_CFD_UTIL.k_PRICE_CARBON, 'Month', 'Commodity Price');

END;
/

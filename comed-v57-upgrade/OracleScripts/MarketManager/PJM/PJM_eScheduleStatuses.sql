DECLARE
    PROCEDURE ADD_TXN_STATUS(v_STATUS_NAME IN VARCHAR2, v_IS_ACTIVE IN NUMBER) IS
    BEGIN
        MERGE INTO INTERCHANGE_TRANSACTION_STATUS I
        USING (SELECT v_STATUS_NAME TRANSACTION_STATUS_NAME, v_IS_ACTIVE TRANSACTION_IS_ACTIVE FROM DUAL) A
        ON (I.TRANSACTION_STATUS_NAME = A.TRANSACTION_STATUS_NAME)
        WHEN NOT MATCHED THEN
            INSERT (TRANSACTION_STATUS_NAME, TRANSACTION_IS_ACTIVE) VALUES (v_STATUS_NAME, v_IS_ACTIVE);    
    END ADD_TXN_STATUS;
BEGIN
	ADD_TXN_STATUS ('Approved', 1);
    ADD_TXN_STATUS ('Requires Apvl', 1);
    ADD_TXN_STATUS ('Requires CP Apvl', 1);
END;
/

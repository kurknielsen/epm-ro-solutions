
DECLARE
    p_STATUS NUMBER;
    v_IO_TABLE_ID NUMBER(9);
    v_MODULE_ID NUMBER(9);
    v_IO_FIELD_ID NUMBER(9);
BEGIN
    SO.ID_FOR_SYSTEM_OBJECT(0,'Entity Manager',0,'Module','Default',TRUE,v_MODULE_ID);

    SO.ID_FOR_SYSTEM_OBJECT(v_MODULE_ID,'TRANSACTION',0,'IO Table','Default',TRUE,v_IO_TABLE_ID);
    IF v_IO_TABLE_ID > 0 THEN
	  SO.ID_FOR_SYSTEM_OBJECT(v_IO_TABLE_ID,'AGREEMENT_TYPE',0,'IO Field','Default',TRUE,v_IO_FIELD_ID);
	  SO.PUT_SYSTEM_OBJECT_ATTRIBUTE(v_IO_FIELD_ID, 107, 'MM.GET_AGREEMENT_TYPE_LIST;TRANSACTION_TYPE;IS_IMPORT_EXPORT;SC_ID;COMMODITY_ID',p_STATUS);
	  DELETE SYSTEM_OBJECT_ATTRIBUTE WHERE OBJECT_ID = v_IO_FIELD_ID AND ATTRIBUTE_ID = 103; --Clear out an existing combo list.

	  SO.ID_FOR_SYSTEM_OBJECT(v_IO_TABLE_ID,'APPROVAL_TYPE',0,'IO Field','Default',TRUE,v_IO_FIELD_ID);
	  SO.PUT_SYSTEM_OBJECT_ATTRIBUTE(v_IO_FIELD_ID, 103, 'BuyerAutoApproval|SellerAutoApproval|CounterpartyApproval',p_STATUS);

	  SO.ID_FOR_SYSTEM_OBJECT(v_IO_TABLE_ID,'LOSS_OPTION',0,'IO Field','Default',TRUE,v_IO_FIELD_ID);
	  SO.PUT_SYSTEM_OBJECT_ATTRIBUTE(v_IO_FIELD_ID, 103, 'BuyerPays|SellerPays',p_STATUS);
    END IF;

END;
/


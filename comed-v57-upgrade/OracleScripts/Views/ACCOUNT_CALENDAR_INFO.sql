CREATE OR REPLACE VIEW ACCOUNT_CALENDAR_INFO ( ACCOUNT_ID, 
CALENDAR_TYPE, BEGIN_DATE, END_DATE, CALENDAR_ID, 
ENTRY_DATE, CALENDAR_NAME ) AS 
SELECT A.ACCOUNT_ID, A.CALENDAR_TYPE, A.BEGIN_DATE, A.END_DATE, A.CALENDAR_ID, 
A.ENTRY_DATE, C.CALENDAR_NAME
FROM ACCOUNT_CALENDAR A, CALENDAR C
WHERE A.CALENDAR_ID = C.CALENDAR_ID;


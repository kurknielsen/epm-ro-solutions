CREATE OR REPLACE VIEW PSE_INVOICE_MISC ( PSE_ID, 
INVOICE_MONTH, PRODUCT_ID, MISC_AMOUNT, STATEMENT_TYPE
 ) AS 
SELECT ENTITY_ID, A.BEGIN_DATE "INVOICE_MONTH", PRODUCT_ID, SUM(LINE_ITEM_AMOUNT), A.STATEMENT_TYPE
FROM INVOICE A, INVOICE_LINE_ITEM B
WHERE PRODUCT_ID = -99 AND A.INVOICE_ID = B.INVOICE_ID
GROUP BY ENTITY_ID, A.BEGIN_DATE, PRODUCT_ID, A.STATEMENT_TYPE;

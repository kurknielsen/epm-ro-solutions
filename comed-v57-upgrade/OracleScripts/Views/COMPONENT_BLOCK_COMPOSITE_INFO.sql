CREATE OR REPLACE VIEW COMPONENT_BLOCK_COMPOSITE_INFO ( COMPONENT_ID, 
BEGIN_DATE, END_DATE, BLOCK_ID, BLOCK_MIN, 
BLOCK_MAX, ENTRY_DATE, COMPONENT_NAME, COMPOSITE_COMPONENT_ID
 ) AS SELECT B.COMPONENT_ID, B.BEGIN_DATE, B.END_DATE,  
	   -1, B.BLOCK_MIN, B.BLOCK_MAX, B.ENTRY_DATE,  
		A.COMPONENT_NAME, B.COMPOSITE_COMPONENT_ID
FROM COMPONENT_COMPOSITE B, COMPONENT A  
WHERE B.COMPOSITE_COMPONENT_ID = A.COMPONENT_ID;

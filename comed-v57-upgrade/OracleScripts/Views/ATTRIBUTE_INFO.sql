CREATE OR REPLACE VIEW ATTRIBUTE_INFO ( ATTRIBUTE_NAME, 
ATTRIBUTE_ID, ENTITY_DOMAIN_ID, ATTRIBUTE_TYPE, ATTRIBUTE_SHOW, 
BEGIN_DATE, END_DATE, ATTRIBUTE_VAL, ENTRY_DATE, 
OWNER_ENTITY_ID ) AS 
SELECT E.ATTRIBUTE_NAME, E.ATTRIBUTE_ID, E.ENTITY_DOMAIN_ID, E.ATTRIBUTE_TYPE, E.ATTRIBUTE_SHOW,
	T.BEGIN_DATE, T.END_DATE, T.ATTRIBUTE_VAL, T.ENTRY_DATE, T.OWNER_ENTITY_ID
FROM TEMPORAL_ENTITY_ATTRIBUTE T, ENTITY_ATTRIBUTE E
WHERE T.ATTRIBUTE_ID = E.ATTRIBUTE_ID;

CREATE OR REPLACE VIEW CALENDAR_PROFILE_INFO ( CALENDAR_ID, 
BEGIN_DATE, PROFILE_ID, END_DATE, PROFILE_NAME, 
PROFILE_TYPE, PROFILE_OPERATION, PROFILE_ORIGIN, PROFILE_LIBRARY_NAME
 ) AS 
SELECT C.CALENDAR_ID, C.BEGIN_DATE, C.PROFILE_ID, C.END_DATE,
	L.PROFILE_NAME, L.PROFILE_TYPE, L.PROFILE_OPERATION,
	L.PROFILE_ORIGIN, LPL.PROFILE_LIBRARY_NAME
FROM CALENDAR_PROFILE C, LOAD_PROFILE L, LOAD_PROFILE_LIBRARY LPL
WHERE C.PROFILE_ID = L.PROFILE_ID AND L.PROFILE_LIBRARY_ID = LPL.PROFILE_LIBRARY_ID;


CREATE OR REPLACE VIEW SERVICE_LOCATION_MRSP_INFO ( SERVICE_LOCATION_ID, 
MRSP_ID, BEGIN_DATE, END_DATE, MRSP_ACCOUNT_NUMBER, 
METER_READ_CYCLE, ENTRY_DATE, MRSP_NAME ) AS SELECT S.SERVICE_LOCATION_ID, S.MRSP_ID, S.BEGIN_DATE, S.END_DATE, S.MRSP_ACCOUNT_NUMBER, 
S.METER_READ_CYCLE, S.ENTRY_DATE, M.MRSP_NAME 
FROM SERVICE_LOCATION_MRSP S, METER_READING_SERVICE_PROVIDER M 
WHERE S.MRSP_ID = M.MRSP_ID;


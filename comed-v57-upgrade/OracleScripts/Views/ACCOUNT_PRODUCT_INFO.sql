CREATE OR REPLACE VIEW ACCOUNT_PRODUCT_INFO ( ACCOUNT_ID, 
PRODUCT_ID, BEGIN_DATE, END_DATE, PRODUCT_TYPE, 
ENTRY_DATE, PRODUCT_NAME ) AS SELECT A.ACCOUNT_ID, A.PRODUCT_ID, A.BEGIN_DATE, A.END_DATE, A.PRODUCT_TYPE, A.ENTRY_DATE, P.PRODUCT_NAME 
FROM ACCOUNT_PRODUCT A, PRODUCT P 
WHERE A.PRODUCT_ID = P.PRODUCT_ID;


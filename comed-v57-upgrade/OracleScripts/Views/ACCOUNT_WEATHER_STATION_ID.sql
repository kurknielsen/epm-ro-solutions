CREATE OR REPLACE VIEW ACCOUNT_WEATHER_STATION_ID ( ACCOUNT_ID, 
SERVICE_LOCATION_ID, BEGIN_DATE, END_DATE, EDC_IDENTIFIER, 
ESP_IDENTIFIER, ENTRY_DATE, SERVICE_LOCATION_NAME, WEATHER_STATION_ID
 ) AS 
SELECT A.ACCOUNT_ID, A.SERVICE_LOCATION_ID, A.BEGIN_DATE, A.END_DATE, 
A.EDC_IDENTIFIER, A.ESP_IDENTIFIER, A.ENTRY_DATE, S.SERVICE_LOCATION_NAME, S.WEATHER_STATION_ID 
FROM ACCOUNT_SERVICE_LOCATION A, SERVICE_LOCATION S 
WHERE A.SERVICE_LOCATION_ID = S.SERVICE_LOCATION_ID;

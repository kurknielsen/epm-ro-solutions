CREATE OR REPLACE VIEW COMPONENT_COINCIDENT_PEAK_INFO ( COMPONENT_ID, 
BEGIN_DATE, END_DATE, A_SYSTEM_LOAD_ID, B_SYSTEM_LOAD_ID, 
RATE, ENTRY_DATE, A_SYSTEM_LOAD_NAME, B_SYSTEM_LOAD_NAME
 ) AS 
SELECT C.COMPONENT_ID, C.BEGIN_DATE, C.END_DATE, C.A_SYSTEM_LOAD_ID,
	C.B_SYSTEM_LOAD_ID,	C.RATE,C.ENTRY_DATE, 
	A.SYSTEM_LOAD_NAME AS A_SYSTEM_LOAD_NAME,
	B.SYSTEM_LOAD_NAME AS B_SYSTEM_LOAD_NAME
FROM COMPONENT_COINCIDENT_PEAK C, SYSTEM_LOAD A, SYSTEM_LOAD B
WHERE C.A_SYSTEM_LOAD_ID = A.SYSTEM_LOAD_ID(+)
	AND C.B_SYSTEM_LOAD_ID = B.SYSTEM_LOAD_ID(+);

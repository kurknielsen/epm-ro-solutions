CREATE OR REPLACE VIEW METER_SCHEDULE_GROUP_INFO ( METER_ID, 
SCHEDULE_GROUP_ID, BEGIN_DATE, END_DATE, ENTRY_DATE, 
SCHEDULE_GROUP_NAME ) AS SELECT M.METER_ID, M.SCHEDULE_GROUP_ID, M.BEGIN_DATE, M.END_DATE, M.ENTRY_DATE, S.SCHEDULE_GROUP_NAME 
FROM METER_SCHEDULE_GROUP M, SCHEDULE_GROUP S 
WHERE M.SCHEDULE_GROUP_ID = S.SCHEDULE_GROUP_ID;


CREATE OR REPLACE VIEW TYPICAL_DAY_LIBRARIES ( PROFILE_LIBRARY_ID, 
PROFILE_LIBRARY_NAME ) AS SELECT DISTINCT LPLIB.PROFILE_LIBRARY_ID, LPLIB.PROFILE_LIBRARY_NAME 
 FROM LOAD_PROFILE LP, LOAD_PROFILE_LIBRARY LPLIB 
WHERE UPPER(LP.PROFILE_TYPE) = 'TYPICAL DAY' AND LP.PROFILE_LIBRARY_ID = LPLIB.PROFILE_LIBRARY_ID;


CREATE OR REPLACE VIEW ENTITY_DOMAIN_PROPERTY AS
SELECT E.ENTITY_DOMAIN_ID, E.ENTITY_DOMAIN_NAME, E.ENTITY_DOMAIN_TABLE, E.ENTITY_DOMAIN_TABLE_ALIAS, 
	N.PRIMARY_ID_COLUMN "PRIMARY_ID_COLUMN", 
	N.SECONDARY_ID_COLUMN "SECONDARY_ID_COLUMN", 
	SUBSTR(N.PRIMARY_ID_COLUMN, 1, LENGTH(TRIM(N.PRIMARY_ID_COLUMN))-3) || '_NAME' "PRIMARY_NAME_COLUMN",
	SUBSTR(N.PRIMARY_ID_COLUMN, 1, LENGTH(TRIM(N.PRIMARY_ID_COLUMN))-3) || '_ALIAS' "PRIMARY_ALIAS_COLUMN",
	PRIMARY_IDENT_COLUMN "PRIMARY_IDENT_COLUMN"
FROM ENTITY_DOMAIN E, NERO_TABLE_PROPERTY_INDEX N
WHERE E.ENTITY_DOMAIN_TABLE = N.TABLE_NAME;

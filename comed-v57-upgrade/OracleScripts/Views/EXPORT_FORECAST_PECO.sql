CREATE OR REPLACE VIEW EXPORT_FORECAST_PECO
	(
	ACCOUNT_NAME,
	ACCOUNT_ID,
	SERVICE_ID,
	THE_DATE,
	THE_VAL,
	EDC_ID,
	ESP_ID,
	EDC_ACCOUNT_NUMBER,
	PECO_SUCCESS_NAME,
	THE_AS_OF_DATE
	) AS
SELECT R.ACCOUNT_NAME,
	A.ACCOUNT_ID,
	S.SERVICE_ID,
	F.LOAD_DATE "THE_DATE",
	F.LOAD_VAL "THE_VAL",
	P.EDC_ID,
	P.ESP_ID,
	E.EDC_ACCOUNT_NUMBER,
	Y.VALUE "PECO_SUCCESS_NAME",
	S.AS_OF_DATE "THE_AS_OF_DATE"
FROM ACCOUNT_SERVICE A,	SERVICE S, PROVIDER_SERVICE P,
	SERVICE_LOAD F, ACCOUNT R, ACCOUNT_EDC E,
	ENERGY_SERVICE_PROVIDER D, SYSTEM_DICTIONARY Y
WHERE S.SERVICE_ID = F.SERVICE_ID
    AND S.ACCOUNT_SERVICE_ID = A.ACCOUNT_SERVICE_ID
    AND S.PROVIDER_SERVICE_ID = P.PROVIDER_SERVICE_ID
    AND A.ACCOUNT_ID = R.ACCOUNT_ID
    AND E.ACCOUNT_ID = A.ACCOUNT_ID
    AND E.EDC_ID = P.EDC_ID
    AND D.ESP_ID = P.ESP_ID
    AND Y.MODEL_ID = 0
	AND Y.MODULE = '?'
    AND Y.KEY1 = '?'
    AND Y.KEY2 = '?'
    AND Y.KEY3 = '?'
    AND Y.SETTING_NAME = 'PECO_SUCCESS_' || D.ESP_NAME
    AND F.LOAD_DATE >= E.BEGIN_DATE
    AND (F.LOAD_DATE <= E.END_DATE OR E.END_DATE IS NULL)
    AND F.SERVICE_CODE = 'F'
    AND F.LOAD_CODE = '1';

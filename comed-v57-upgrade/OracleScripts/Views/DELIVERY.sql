CREATE OR REPLACE VIEW DELIVERY ( EDC_ID, 
ESP_ID, POOL_ID, DELIVERY_TYPE, DELIVERY_DATE, 
AS_OF_DATE, DELIVERY_VOL ) AS SELECT EDC_ID, SELLER_ID, POOL_ID,
SCHEDULE_TYPE, SCHEDULE_DATE, AS_OF_DATE, AMOUNT
FROM INTERCHANGE_TRANSACTION A, IT_COMMODITY B, IT_SCHEDULE C
WHERE UPPER(TRANSACTION_TYPE) = 'LOAD'
	AND UPPER(B.COMMODITY_TYPE) = 'GAS'
	AND A.COMMODITY_ID = B.COMMODITY_ID
	AND C.TRANSACTION_ID = A.TRANSACTION_ID;


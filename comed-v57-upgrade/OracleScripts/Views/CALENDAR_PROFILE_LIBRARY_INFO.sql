CREATE OR REPLACE VIEW CALENDAR_PROFILE_LIBRARY_INFO ( CALENDAR_ID, 
BEGIN_DATE, PROFILE_LIBRARY_ID, END_DATE, PROFILE_LIBRARY_NAME
 ) AS 
SELECT C.CALENDAR_ID, C.BEGIN_DATE, C.PROFILE_LIBRARY_ID, C.END_DATE, L.PROFILE_LIBRARY_NAME
FROM CALENDAR_PROFILE_LIBRARY C, LOAD_PROFILE_LIBRARY L
WHERE C.PROFILE_LIBRARY_ID = L.PROFILE_LIBRARY_ID;

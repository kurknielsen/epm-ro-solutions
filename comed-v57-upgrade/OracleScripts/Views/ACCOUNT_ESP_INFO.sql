CREATE OR REPLACE VIEW ACCOUNT_ESP_INFO ( ACCOUNT_ID, 
ESP_ID, BEGIN_DATE, END_DATE, ESP_ACCOUNT_NUMBER, 
ENTRY_DATE, ESP_NAME, POOL_ID, POOL_NAME
 ) AS SELECT A.ACCOUNT_ID, A.ESP_ID, A.BEGIN_DATE, A.END_DATE, 
	A.ESP_ACCOUNT_NUMBER, A.ENTRY_DATE, E.ESP_NAME, A.POOL_ID, 
	P.POOL_NAME 
FROM ACCOUNT_ESP A, ENERGY_SERVICE_PROVIDER E, POOL P 
WHERE A.ESP_ID = E.ESP_ID AND A.POOL_ID = P.POOL_ID;


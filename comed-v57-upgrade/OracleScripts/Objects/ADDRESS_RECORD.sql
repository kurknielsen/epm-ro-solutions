CREATE OR REPLACE TYPE ADDRESS_RECORD AS OBJECT
(
--Revision: $Revision: 1.5 $
	-- Author  : JHUMPHRIES
	-- Created : 6/19/2004 9:14:23 AM
	-- Purpose : Address record that has fields for each piece of detail (whereas table has a single GEOGRAPHY_ID)
	ADDRESS_NAME VARCHAR2(64),
	ADDRESS_ALIAS VARCHAR2(32),
	ADDRESS_DESC VARCHAR2(256),
	STREET VARCHAR2(64),
	STREET2 VARCHAR2(64),
    CITY VARCHAR2(32),
    STATE_CODE VARCHAR2(16),
    POSTAL_CODE VARCHAR2(16),
    COUNTRY_CODE VARCHAR2(32),
	EXTERNAL_IDENTIFIER VARCHAR2(64)
);
/

CREATE OR REPLACE TYPE ESP_SYNC_TYPE AS OBJECT
(
--Revision: $Revision: 1.17 $
  ESP_NAME VARCHAR2(32),
  ESP_ALIAS VARCHAR2(32),
  ESP_EXTERNAL_IDENTIFIER VARCHAR2(64),
  ESP_DUNS_NUMBER VARCHAR2(16),
  ESP_STATUS VARCHAR2(16),
  ESP_TYPE VARCHAR2(16),
  ESP_USE_USAGE_DAY_PROFILE NUMBER (1),
  ESP_EXCLUDE_LOAD_SCHEDULE NUMBER (1)
);
/

CREATE OR REPLACE TYPE ESP_SYNC_TABLE IS TABLE OF ESP_SYNC_TYPE;

CREATE OR REPLACE TRIGGER INTERCHANGE_CONTRACT_DELETE
	AFTER DELETE ON INTERCHANGE_CONTRACT
	FOR EACH ROW
BEGIN
	DELETE TP_CONTRACT_NUMBER WHERE CONTRACT_ID = :old.CONTRACT_ID;
	DELETE CONTRACT_PRODUCT_COMPONENT WHERE CONTRACT_ID = :old.CONTRACT_ID;
	UPDATE INTERCHANGE_TRANSACTION SET CONTRACT_ID = 0 WHERE CONTRACT_ID = :old.CONTRACT_ID;
END INTERCHANGE_CONTRACT_DELETE;
/

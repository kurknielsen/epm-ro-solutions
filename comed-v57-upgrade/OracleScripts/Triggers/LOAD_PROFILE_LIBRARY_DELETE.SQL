CREATE OR REPLACE TRIGGER LOAD_PROFILE_LIBRARY_DELETE
	AFTER DELETE ON LOAD_PROFILE_LIBRARY
	FOR EACH ROW
BEGIN
	DELETE CALENDAR_PROFILE_LIBRARY WHERE PROFILE_LIBRARY_ID = :old.PROFILE_LIBRARY_ID;
	DELETE LOAD_PROFILE WHERE PROFILE_LIBRARY_ID = :old.PROFILE_LIBRARY_ID;
END LOAD_PROFILE_LIBRARY_DELETE;
/

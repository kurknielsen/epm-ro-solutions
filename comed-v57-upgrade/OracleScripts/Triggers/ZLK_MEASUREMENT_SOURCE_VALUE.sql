CREATE OR REPLACE TRIGGER ZLK_MEASUREMENT_SOURCE_VALUE
	BEFORE INSERT OR DELETE OR UPDATE
	ON MEASUREMENT_SOURCE_VALUE
	FOR EACH ROW
DECLARE
	v_LOCK_STATE CHAR(1);
BEGIN
	IF UPDATING THEN
	
		-- attempts to set LOCK_STATE to NULL will silently do nothing
		:new.LOCK_STATE := NVL(:new.LOCK_STATE, :old.LOCK_STATE);
	
		-- did key columns change? If so, we need to check old and new
		-- lock states
		IF NOT ((:new.MEASUREMENT_SOURCE_ID IS NULL AND :old.MEASUREMENT_SOURCE_ID IS NULL) OR :new.MEASUREMENT_SOURCE_ID = :old.MEASUREMENT_SOURCE_ID) OR
		   NOT ((:new.SOURCE_DATE IS NULL AND :old.SOURCE_DATE IS NULL) OR :new.SOURCE_DATE = :old.SOURCE_DATE) THEN
		
			SECURITY_CONTROLS.ENFORCE_LOCK_STATE(:old.LOCK_STATE);
		
			-- query for new lock state
			SELECT MIN(LOCK_STATE)
			INTO v_LOCK_STATE
			FROM MEASUREMENT_SRC_VAL_LK_SUMMARY
			WHERE MEASUREMENT_SOURCE_ID = :new.MEASUREMENT_SOURCE_ID
				  AND BEGIN_DATE <= :new.SOURCE_DATE
				  AND END_DATE >= :new.SOURCE_DATE;
		
			SECURITY_CONTROLS.ENFORCE_LOCK_STATE(v_LOCK_STATE);
			:new.LOCK_STATE := NVL(v_LOCK_STATE,'U');
		
		-- only do something if this is not a no-op update
		ELSIF NOT ((:new.SOURCE_VALUE IS NULL AND :old.SOURCE_VALUE IS NULL) OR :new.SOURCE_VALUE = :old.SOURCE_VALUE) OR
			  NOT ((:new.SOURCE_QUAL_CODE IS NULL AND :old.SOURCE_QUAL_CODE IS NULL) OR :new.SOURCE_QUAL_CODE = :old.SOURCE_QUAL_CODE) THEN
		
			SECURITY_CONTROLS.ENFORCE_LOCK_STATE(:new.LOCK_STATE);
		
		ELSE
			-- no-op update? then make sure entry date is not changed
			:new.ENTRY_DATE := :old.ENTRY_DATE;
		
		END IF;
		
	ELSIF INSERTING THEN
	
		-- query for lock state
		SELECT MIN(LOCK_STATE)
		INTO v_LOCK_STATE
		FROM MEASUREMENT_SRC_VAL_LK_SUMMARY
		WHERE MEASUREMENT_SOURCE_ID = :new.MEASUREMENT_SOURCE_ID
			  AND BEGIN_DATE <= :new.SOURCE_DATE
			  AND END_DATE >= :new.SOURCE_DATE;
	
		SECURITY_CONTROLS.ENFORCE_LOCK_STATE(v_LOCK_STATE);
		:new.LOCK_STATE := NVL(v_LOCK_STATE,'U');
	
	ELSE -- must be DELETING
	
		SECURITY_CONTROLS.ENFORCE_LOCK_STATE(:old.LOCK_STATE);
	
	END IF;

END ZLK_MEASUREMENT_SOURCE_VALUE;
/

CREATE OR REPLACE TRIGGER LOAD_PROFILE_DELETE
	AFTER DELETE ON LOAD_PROFILE
	FOR EACH ROW
BEGIN
	DELETE CALENDAR_PROFILE WHERE PROFILE_ID = :old.PROFILE_ID;
	DELETE LOAD_PROFILE_POINT WHERE PROFILE_ID = :old.PROFILE_ID;
	DELETE LOAD_PROFILE_STATISTICS WHERE PROFILE_ID = :old.PROFILE_ID;
	DELETE LOAD_PROFILE_WRF WHERE PROFILE_ID = :old.PROFILE_ID;
	DELETE LOAD_PROFILE_WRF_WEATHER WHERE PROFILE_ID = :old.PROFILE_ID;
END LOAD_PROFILE_DELETE;
/
